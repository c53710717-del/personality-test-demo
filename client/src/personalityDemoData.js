function pickExamples(direction, type) {
  if (Array.isArray(type.examples) && type.examples.length) {
    return type.examples.slice(0, 3);
  }

  const theme = getTheme(direction);
  const seed = type.code.split("").reduce((sum, char) => sum + char.charCodeAt(0), 0);
  const ordered = theme.examples.map((item, index) => ({
    ...item,
    order: (seed + index * 7) % theme.examples.length
  }));
  return ordered.sort((a, b) => a.order - b.order).slice(0, 3);
}

