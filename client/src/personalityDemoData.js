export function shouldRequestGeneratedSystem(config) {
  const normalized = normalizeConfig(config);
  if (normalized.generatedSystem) {
    return Array.isArray(normalized.generatedSystem.archetypes)
      ? normalized.generatedSystem.archetypes.length !== Number(normalized.typeCount)
      : true;
  }
  if (normalized.customDirection) return true;
  const direction = getSelectedDirections(normalized)[0];
  if (!direction) return false;
  const nativeSystem = nativeDirectionSystems[direction.id];
  if (!nativeSystem) return true;
  return nativeSystem.archetypes.length !== Number(normalized.typeCount);
}

function distributeCounts(total, bucketCount) {
  const base = Math.floor(total / bucketCount);
  const remainder = total % bucketCount;
  return Array.from({ length: bucketCount }, (_, index) => base + (index < remainder ? 1 : 0));
}

function getAxisCountForTypeCount(typeCount) {
  if (typeCount >= 32) return 5;
  if (typeCount >= 16) return 4;
  if (typeCount >= 8) return 3;
  return 2;
}

